def area(a):
    '''
    функция для высчитывания площади квадрата

    если a (сторона) это число, то возводит в квадрат
    иначе выводит ничего (None)

    Example:
    int a = 5

    return: 25
    '''
    return a * a

def perimeter(a):
    '''
    функция для высчитывания периметра квадрата

    если a (сторона) это число, то умножает на 4
    иначе выводит ничего (None)

    Example:
    int r = 5

    return: 20
    '''
    return 4 * a
